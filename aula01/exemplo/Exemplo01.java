package exemplo;

/**
 Comentário de Bloco
 */
public class Exemplo01 {
    public static void main(String[] args) {
        System.out.print("Ola Mundo");
        System.out.println("Primeiro \nPrograma"); // \n é comentário de linha

        System.out.println("2 + 3");
        System.out.println(2 + 3);

        System.out.println("Resposta = " + 2 + 3);
        System.out.println("Resposta = " + (2 + 3));
        System.out.println("Quando é 2 + 3 ?\n" + "Resposta: " + (2 + 3));
    
        
    }
    
}

